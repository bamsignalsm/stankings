function chapters(bookId: string, titles: string[]): GovernanceBookChapter[] {
  return titles.map((title, i) => {
    const num = i + 1;
    const id = `${bookId}-ch-${String(num).padStart(2, "0")}`;
    return {
      id,
      chapter: `Chapter ${num}`,
      title,
      status: "planned" as const,
      href:
        bookId === "book-i"
          ? `/library/governance-code/book-i/chapters/${id}`
          : `/library/governance-code/${bookId}`,
    };
  });
}

