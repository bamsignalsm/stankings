export function getGovernanceBook(id: string): GovernanceBook | undefined {
  return GOVERNANCE_CODE_BOOKS.find((b) => b.id === id);
}

export function getGovernanceBookStats() {
  const total = GOVERNANCE_CODE_BOOKS.length;
  const planning = GOVERNANCE_CODE_BOOKS.filter((b) => b.status === "planning").length;
  const drafting = GOVERNANCE_CODE_BOOKS.filter((b) => b.status === "drafting").length;
  const chapters = GOVERNANCE_CODE_BOOKS.reduce((n, b) => n + b.chapters.length, 0);
  return { total, planning, drafting, chapters };
}
