export const PREAMBLE = `We, the founders of Stankings Group, establish this Constitution to preserve an institution that shall endure beyond our lifetime.

This Constitution exists to provide a permanent foundation upon which businesses, technologies, institutions, charitable initiatives, educational establishments, and future generations may continue to build.

It exists because companies may rise and fall, economies may change, governments may change, technologies may become obsolete, and generations may come and go, but principles, when carefully preserved, can endure.

Stankings Group is therefore established not merely as a collection of companies, but as an enduring institution dedicated to creating value, solving meaningful problems, developing people, advancing society, and leaving every generation better than it found the last.

This Constitution shall serve as the highest internal governing document of Stankings Group. Every policy, strategy, investment, product, technology, company, subsidiary, and future institution shall align with the principles contained herein. No temporary circumstance shall override the permanent values upon which this institution is built.`;

